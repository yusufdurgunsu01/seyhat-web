// Initialize the map
function initMap() {
    // Create map centered on Turkey with a wider view
    const map = L.map('map').setView([39.9334, 32.8597], 6);

    // Add OpenStreetMap tile layer
    L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
        attribution: '© OpenStreetMap contributors',
        maxZoom: 19
    }).addTo(map);

    // Custom popup style
    const customPopup = L.popup({
        maxWidth: 300,
        className: 'custom-popup'
    });

    // Store the GeoJSON layer for later use
    let geoJsonLayer;
    let currentPopup = null;

    // Add GeoJSON data to the map with custom styling
    geoJsonLayer = L.geoJSON(geoJsonData, {
        style: function(feature) {
            return {
                fillColor: '#4CAF50',
                fillOpacity: 0.3,
                color: '#4CAF50',
                weight: 2,
                opacity: 0.8
            };
        },
        onEachFeature: function(feature, layer) {
            const cityName = feature.properties.name;
            const cityData = cityInfo[cityName];

            // Add hover effects with detailed info
            layer.on('mouseover', function(e) {
                const cityName = feature.properties.name;
                const cityData = cityInfo[cityName];

                this.setStyle({
                    fillOpacity: 0.5,
                    weight: 3
                });

                // Elazığ için özel hover içeriği
                if (cityName === "Elazig") {
                    const hoverContent = `
                        <div class="city-hover-info" style="padding: 10px;">
                            <div style="text-align: center; margin-bottom: 10px;">
                                <img src="./view/images/elazig.jpg" style="width: 100%; height: 150px; object-fit: cover; border-radius: 8px; margin-bottom: 10px;">
                                <h5 style="margin: 0; color: #2a5298;">Elazığ</h5>
                            </div>
                            <div style="font-size: 0.9em;">
                                <p style="margin: 5px 0;"><i class="fas fa-landmark"></i> Harput Kalesi</p>
                                <p style="margin: 5px 0;"><i class="fas fa-mountain"></i> Hazar Gölü</p>
                                <p style="margin: 5px 0;"><i class="fas fa-utensils"></i> Orcik şekeri</p>
                            </div>
                            <div style="text-align: center; margin-top: 10px;">
                                <small style="color: #666;">Detaylar için tıklayın</small>
                            </div>
                        </div>
                    `;

                    if (currentPopup) {
                        map.closePopup(currentPopup);
                    }

                    currentPopup = L.popup({
                        maxWidth: 300,
                        className: 'custom-popup'
                    })
                    .setLatLng(e.latlng)
                    .setContent(hoverContent)
                    .openOn(map);
                    return;
                }

                // Diğer iller için normal hover popup
                if (cityData) {
                    const hoverContent = `
                        <div class="city-hover-info" style="padding: 10px;">
                            <div style="text-align: center; margin-bottom: 10px;">
                                <img src="${cityData.image}" style="width: 100%; height: 150px; object-fit: cover; border-radius: 8px; margin-bottom: 10px;">
                                <h5 style="margin: 0; color: #2a5298;">${cityName}</h5>
                            </div>
                            <div style="font-size: 0.9em;">
                                <p style="margin: 5px 0;"><i class="fas fa-landmark"></i> ${cityData.info["Tarihi Yerler"][0]}</p>
                                <p style="margin: 5px 0;"><i class="fas fa-mountain"></i> ${cityData.info["Doğal Güzellikler"][0]}</p>
                                <p style="margin: 5px 0;"><i class="fas fa-utensils"></i> ${cityData.info["Yerel Lezzetler"][0]}</p>
                            </div>
                            <div style="text-align: center; margin-top: 10px;">
                                <small style="color: #666;">Detaylar için tıklayın</small>
                            </div>
                        </div>
                    `;

                    if (currentPopup) {
                        map.closePopup(currentPopup);
                    }

                    currentPopup = L.popup({
                        maxWidth: 300,
                        className: 'custom-popup'
                    })
                    .setLatLng(e.latlng)
                    .setContent(hoverContent)
                    .openOn(map);
                }
            });

            layer.on('mouseout', function() {
                this.setStyle({
                    fillOpacity: 0.3,
                    weight: 2
                });
                if (currentPopup) {
                    map.closePopup(currentPopup);
                    currentPopup = null;
                }
            });

            // Add click event with enhanced popup
            layer.on('click', function(e) {
                const bounds = layer.getBounds();
                
                // Zoom to the feature with animation
                map.fitBounds(bounds, {
                    padding: [50, 50],
                    maxZoom: 10,
                    animate: true,
                    duration: 1
                });

                if (cityData) {
                    // Create enhanced popup content
                    const popupContent = `
                        <div class="city-click-info" style="padding: 15px;">
                            <div style="text-align: center; margin-bottom: 15px;">
                                <img src="${cityData.image}" style="width: 100%; height: 200px; object-fit: cover; border-radius: 8px; margin-bottom: 15px;">
                                <h4 style="margin: 0; color: #2a5298;">${cityName}</h4>
                                <p style="color: #666; margin: 5px 0;">${cityData.info["Tarihi Önemi"][0]}</p>
                            </div>
                            <div style="display: grid; grid-template-columns: 1fr 1fr; gap: 10px; font-size: 0.9em;">
                                <div>
                                    <h6 style="color: #2a5298; margin: 5px 0;"><i class="fas fa-landmark"></i> Tarihi Yerler</h6>
                                    <ul style="padding-left: 20px; margin: 0;">
                                        ${cityData.info["Tarihi Yerler"].slice(0, 3).map(place => 
                                            `<li>${place}</li>`
                                        ).join('')}
                                    </ul>
                                </div>
                                <div>
                                    <h6 style="color: #2a5298; margin: 5px 0;"><i class="fas fa-mountain"></i> Doğal Güzellikler</h6>
                                    <ul style="padding-left: 20px; margin: 0;">
                                        ${cityData.info["Doğal Güzellikler"].slice(0, 3).map(place => 
                                            `<li>${place}</li>`
                                        ).join('')}
                                    </ul>
                                </div>
                            </div>
                            <div style="text-align: center; margin-top: 15px;">
                                <button onclick="showCityInfo('${cityName}')" 
                                        style="background-color: #2a5298; color: white; border: none; padding: 8px 15px; 
                                               border-radius: 4px; cursor: pointer;">
                                    Tüm Detayları Gör
                                </button>
                            </div>
                        </div>
                    `;

                    // Show enhanced popup
                    L.popup({
                        maxWidth: 400,
                        className: 'custom-popup'
                    })
                    .setLatLng(e.latlng)
                    .setContent(popupContent)
                    .openOn(map);

                    // Add a "Back to Turkey View" button
                    const backButton = L.control({ position: 'topright' });
                    backButton.onAdd = function() {
                        const div = L.DomUtil.create('div', 'leaflet-bar leaflet-control');
                        div.innerHTML = `
                            <a href="#" title="Türkiye Haritasına Dön" 
                               style="background-color: white; padding: 8px 15px; border-radius: 4px; 
                                      text-decoration: none; color: #333; box-shadow: 0 2px 5px rgba(0,0,0,0.2);">
                                <i class="fas fa-globe"></i> Türkiye'ye Dön
                            </a>
                        `;
                        div.onclick = function(e) {
                            e.preventDefault();
                            map.setView([39.9334, 32.8597], 6, {
                                animate: true,
                                duration: 1
                            });
                            map.closePopup();
                        };
                        return div;
                    };
                    backButton.addTo(map);
                }
            });
        }
    }).addTo(map);

    // Add custom CSS for popups
    const style = document.createElement('style');
    style.textContent = `
        .custom-popup .leaflet-popup-content-wrapper {
            border-radius: 8px;
            box-shadow: 0 3px 10px rgba(0,0,0,0.2);
        }
        .custom-popup .leaflet-popup-content {
            margin: 0;
        }
        .custom-popup .leaflet-popup-tip {
            background: white;
        }
    `;
    document.head.appendChild(style);

    // Add a scale control
    L.control.scale({
        imperial: false,
        metric: true
    }).addTo(map);

    // Add a zoom control
    L.control.zoom({
        position: 'bottomright'
    }).addTo(map);
}

// Call initMap when the page loads
document.addEventListener('DOMContentLoaded', initMap);

